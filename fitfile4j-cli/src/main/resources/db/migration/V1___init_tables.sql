CREATE TABLE SESSION (
    activity_id BIGINT not null,
    avg_speed INT, -- 14 / 124
    total_elapsed_time INT, -- 7
    total_distance INT, -- 9
    FOREIGN KEY (activity_id) REFERENCES FIT_ACTIVITY (id)
);

